package org.blastall.blast.objects;

public class Protein {
	
	private int proteinId;
	private String proteinSequence;
	
	/**
	 * @return the proteinId
	 */
	public int getProteinId() {
		return proteinId;
	}
	/**
	 * @param proteinId the proteinId to set
	 */
	public void setProteinId(int proteinId) {
		this.proteinId = proteinId;
	}
	/**
	 * @return the proteinSequence
	 */
	public String getProteinSequence() {
		return proteinSequence;
	}
	/**
	 * @param proteinSequence the proteinSequence to set
	 */
	public void setProteinSequence(String proteinSequence) {
		this.proteinSequence = proteinSequence;
	}

}
