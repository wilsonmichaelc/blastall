package org.blastall.blast.objects;

public class Property {

	private String blast_dir;
	private String tmp_dir;
	private String db_dir;
	private String post_proteins;
	private String threads;
	private String database;
	private String num_results;
	private String post_xml;
	
	/**
	 * @return the blast_dir
	 */
	public String getBlast_dir() {
		return blast_dir;
	}
	/**
	 * @param blast_dir the blast_dir to set
	 */
	public void setBlast_dir(String blast_dir) {
		this.blast_dir = blast_dir;
	}
	/**
	 * @return the tmp_dir
	 */
	public String getTmp_dir() {
		return tmp_dir;
	}
	/**
	 * @param tmp_dir the tmp_dir to set
	 */
	public void setTmp_dir(String tmp_dir) {
		this.tmp_dir = tmp_dir;
	}
	/**
	 * @return the db_dir
	 */
	public String getDb_dir() {
		return db_dir;
	}
	/**
	 * @param db_dir the db_dir to set
	 */
	public void setDb_dir(String db_dir) {
		this.db_dir = db_dir;
	}
	/**
	 * @return the post_proteins
	 */
	public String getPost_proteins() {
		return post_proteins;
	}
	/**
	 * @param post_proteins the post_proteins to set
	 */
	public void setPost_proteins(String post_proteins) {
		this.post_proteins = post_proteins;
	}
	/**
	 * @return the threads
	 */
	public String getThreads() {
		return threads;
	}
	/**
	 * @param threads the threads to set
	 */
	public void setThreads(String threads) {
		this.threads = threads;
	}
	/**
	 * @return the database
	 */
	public String getDatabase() {
		return database;
	}
	/**
	 * @param database the database to set
	 */
	public void setDatabase(String database) {
		this.database = database;
	}
	/**
	 * @return the num_results
	 */
	public String getNum_results() {
		return num_results;
	}
	/**
	 * @param num_results the num_results to set
	 */
	public void setNum_results(String num_results) {
		this.num_results = num_results;
	}
	/**
	 * @return the post_xml
	 */
	public String getPost_xml() {
		return post_xml;
	}
	/**
	 * @param post_xml the post_xml to set
	 */
	public void setPost_xml(String post_xml) {
		this.post_xml = post_xml;
	}

}
